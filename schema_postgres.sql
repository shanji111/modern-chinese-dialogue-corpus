CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_pattern ON dialogue_pairs(has_pattern_reuse, id);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_question ON dialogue_pairs(has_question_response, id);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_negation ON dialogue_pairs(has_negation_turn, id);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_repair ON dialogue_pairs(has_repair_repetition, id);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_text_a_trgm
    ON dialogue_pairs USING GIN (text_a gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_text_b_trgm
    ON dialogue_pairs USING GIN (text_b gin_trgm_ops);

ALTER TABLE corpus_submissions
    ADD COLUMN IF NOT EXISTS storage_backend TEXT DEFAULT 'local',
    ADD COLUMN IF NOT EXISTS object_key TEXT,
    ADD COLUMN IF NOT EXISTS file_url TEXT,
    ADD COLUMN IF NOT EXISTS file_hash TEXT;

ALTER TABLE multimodal_entries
    ADD COLUMN IF NOT EXISTS storage_backend TEXT DEFAULT 'local',
    ADD COLUMN IF NOT EXISTS object_key TEXT,
    ADD COLUMN IF NOT EXISTS file_url TEXT,
    ADD COLUMN IF NOT EXISTS file_hash TEXT;

CREATE INDEX IF NOT EXISTS idx_submissions_status ON corpus_submissions (status);
CREATE INDEX IF NOT EXISTS idx_submissions_created_at ON corpus_submissions (created_at);
CREATE INDEX IF NOT EXISTS idx_multimodal_submission_id ON multimodal_entries (submission_id);

-- Baseline PostgreSQL search can use ILIKE. These trigram indexes improve ILIKE
-- when pg_trgm is available; if the extension is unavailable, remove only these
-- GIN index statements and the application can still fall back to plain ILIKE.
CREATE INDEX IF NOT EXISTS idx_corpus_title_trgm
    ON corpus_entries USING GIN (title gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_corpus_content_trgm
    ON corpus_entries USING GIN (content gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_corpus_current_segment_trgm
    ON corpus_entries USING GIN (current_segment gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_corpus_speaker_trgm
    ON corpus_entries USING GIN (speaker gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_corpus_search_text_trgm
    ON corpus_entries USING GIN (search_text gin_trgm_ops);
