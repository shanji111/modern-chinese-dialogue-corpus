CREATE INDEX IF NOT EXISTS idx_corpus_crawl_date ON corpus_entries (crawl_date);
CREATE INDEX IF NOT EXISTS idx_corpus_audio_file ON corpus_entries (audio_file);
CREATE INDEX IF NOT EXISTS idx_site_visitors_last_seen ON site_visitors (last_seen_epoch);
CREATE INDEX IF NOT EXISTS idx_site_visitor_daily_day ON site_visitor_daily (day_key);
CREATE INDEX IF NOT EXISTS idx_site_visit_snapshots_bucket ON site_visit_snapshots (bucket_epoch);
