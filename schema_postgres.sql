CREATE EXTENSION IF NOT EXISTS pg_trgm;

CREATE TABLE IF NOT EXISTS corpus_entries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    content TEXT NOT NULL,
    source TEXT NOT NULL,
    year INTEGER,
    category TEXT,
    dataset_name TEXT,
    created_at TIMESTAMP,
    import_batch TEXT,
    content_hash TEXT,
    source_url TEXT,
    crawl_source TEXT,
    crawl_date TEXT,
    license_note TEXT,
    audio_file TEXT,
    segment_text TEXT,
    current_segment TEXT,
    prev_segment TEXT,
    next_segment TEXT,
    start_time DOUBLE PRECISION,
    end_time DOUBLE PRECISION,
    speaker TEXT,
    conversation_id TEXT,
    segment_index INTEGER,
    search_text TEXT
);

CREATE TABLE IF NOT EXISTS corpus_submissions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    submitter_name TEXT,
    submitter_email TEXT,
    title TEXT NOT NULL,
    source TEXT,
    category TEXT NOT NULL,
    genre TEXT,
    language TEXT,
    modality TEXT NOT NULL,
    text_content TEXT,
    original_filename TEXT,
    stored_filename TEXT,
    file_path TEXT,
    file_mime_type TEXT,
    file_size BIGINT,
    storage_backend TEXT DEFAULT 'local',
    object_key TEXT,
    file_url TEXT,
    file_hash TEXT,
    status TEXT DEFAULT 'pending',
    admin_note TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    reviewed_at TIMESTAMP,
    reviewed_by TEXT
);

CREATE TABLE IF NOT EXISTS multimodal_entries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    submission_id BIGINT UNIQUE,
    title TEXT NOT NULL,
    source TEXT,
    category TEXT,
    genre TEXT,
    language TEXT,
    modality TEXT NOT NULL,
    text_content TEXT,
    original_filename TEXT,
    stored_filename TEXT,
    file_path TEXT,
    file_mime_type TEXT,
    file_size BIGINT,
    storage_backend TEXT DEFAULT 'local',
    object_key TEXT,
    file_url TEXT,
    file_hash TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS dialogue_turns (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    entry_id BIGINT NOT NULL,
    turn_index INTEGER NOT NULL,
    speaker_label TEXT,
    turn_text TEXT NOT NULL,
    source TEXT,
    category TEXT,
    dataset_name TEXT,
    conversation_key TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS corpus_stats (
    stat_key TEXT PRIMARY KEY,
    stat_value TEXT,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS site_visitors (
    visitor_hash VARCHAR(64) PRIMARY KEY,
    first_seen_epoch BIGINT NOT NULL,
    last_seen_epoch BIGINT NOT NULL
);

CREATE TABLE IF NOT EXISTS site_visitor_daily (
    day_key VARCHAR(10) NOT NULL,
    visitor_hash VARCHAR(64) NOT NULL,
    first_seen_epoch BIGINT NOT NULL,
    last_seen_epoch BIGINT NOT NULL,
    PRIMARY KEY (day_key, visitor_hash)
);

CREATE TABLE IF NOT EXISTS site_visit_snapshots (
    bucket_epoch BIGINT PRIMARY KEY,
    online_visitors INTEGER NOT NULL,
    peak_online_visitors INTEGER NOT NULL,
    total_visitors BIGINT NOT NULL,
    daily_visitors BIGINT NOT NULL,
    recorded_at_epoch BIGINT NOT NULL
);

CREATE TABLE IF NOT EXISTS site_ip_activity (
    ip_address VARCHAR(45) NOT NULL,
    bucket_epoch BIGINT NOT NULL,
    first_seen_epoch BIGINT NOT NULL,
    last_seen_epoch BIGINT NOT NULL,
    request_count BIGINT NOT NULL,
    blocked_count BIGINT NOT NULL DEFAULT 0,
    last_path VARCHAR(255),
    last_method VARCHAR(12),
    last_status INTEGER,
    PRIMARY KEY (ip_address, bucket_epoch)
);

CREATE TABLE IF NOT EXISTS site_blocked_ips (
    ip_address VARCHAR(45) PRIMARY KEY,
    reason TEXT,
    blocked_at_epoch BIGINT NOT NULL,
    blocked_by VARCHAR(120)
);

CREATE TABLE IF NOT EXISTS dialogue_pairs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    turn_a_id BIGINT NOT NULL,
    turn_b_id BIGINT NOT NULL,
    entry_id BIGINT NOT NULL,
    conversation_key TEXT NOT NULL,
    turn_index_a INTEGER NOT NULL,
    turn_index_b INTEGER NOT NULL,
    speaker_a TEXT,
    speaker_b TEXT,
    text_a TEXT NOT NULL,
    text_b TEXT NOT NULL,
    source TEXT,
    category TEXT,
    dataset_name TEXT,
    shared_terms TEXT,
    markers TEXT,
    has_lexical_echo INTEGER DEFAULT 0,
    has_pattern_reuse INTEGER DEFAULT 0,
    has_question_response INTEGER DEFAULT 0,
    has_negation_turn INTEGER DEFAULT 0,
    has_repair_repetition INTEGER DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE corpus_entries
    ADD COLUMN IF NOT EXISTS search_text TEXT,
    ADD COLUMN IF NOT EXISTS source_url TEXT,
    ADD COLUMN IF NOT EXISTS crawl_source TEXT,
    ADD COLUMN IF NOT EXISTS crawl_date TEXT,
    ADD COLUMN IF NOT EXISTS license_note TEXT;

