CREATE INDEX IF NOT EXISTS idx_dialogue_turns_entry ON dialogue_turns (entry_id);
CREATE INDEX IF NOT EXISTS idx_dialogue_turns_entry_id ON dialogue_turns (entry_id);
CREATE INDEX IF NOT EXISTS idx_dialogue_turns_conversation ON dialogue_turns (conversation_key, turn_index);
CREATE INDEX IF NOT EXISTS idx_dialogue_turns_conversation_turn ON dialogue_turns (conversation_key, turn_index);
CREATE INDEX IF NOT EXISTS idx_dialogue_turns_source ON dialogue_turns (source);
CREATE INDEX IF NOT EXISTS idx_dialogue_turns_category ON dialogue_turns (category);
CREATE INDEX IF NOT EXISTS idx_dialogue_turns_source_category_id ON dialogue_turns (source, category, id);
CREATE INDEX IF NOT EXISTS idx_dialogue_turns_turn_text_trgm
    ON dialogue_turns USING GIN (turn_text gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_a ON dialogue_pairs(turn_a_id);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_b ON dialogue_pairs(turn_b_id);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_entry ON dialogue_pairs(entry_id);
CREATE INDEX IF NOT EXISTS idx_dialogue_pairs_source_category_id ON dialogue_pairs(source, category, id);
